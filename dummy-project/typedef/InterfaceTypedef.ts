/**
 * Interface to use doc directly on property
 * @ko property에 직접 jsdoc 사용 인터페이스
 * @property {number} numberProperty - interface number property <ko>인터페이스 숫자 속성</ko>
 * @property {string} stringProperty - interface string property <ko>인터페이스 문자열 속성</ko>
 * @typedef
 */
interface InterfaceTypedef {
  numberProperty: number;
  stringProperty: string;
}

export default InterfaceTypedef;
